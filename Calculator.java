public class Calculator {

	public int add(int num1, int num2) {
		int result = num1 + num2;
		return result;
	}
	
	public int sub(int num1, int num2) {
		int result = num1 - num2;
		return result;
	}
	public int divide(int num1, int num2) {
		int result = num1 / num2;
		return result;
	}
	
	public int multiply(int n1,int n2)
	{
		int result = n1 * n2;
		return result;
	}
	}